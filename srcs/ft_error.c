#include "ft_ls.h"
#include <unistd.h>

void	ft_error(char *s)
{
	write(2, "ft_ls: ", 7);
	ft_putstr_fd(s, 2);
	write(2, ": ", 2);
	ft_putendl_fd(strerror(errno), 2);
}
